@torch.no_grad()
def evaluate_for_outputs(model, criterion, postprocessors, data_loader, base_ds, device, output_dir):
    model.eval()
    criterion.eval()

    metric_logger = utils.MetricLogger(delimiter="  ")
    metric_logger.add_meter('class_error', utils.SmoothedValue(window_size=1, fmt='{value:.2f}'))
    header = 'Test:'

    iou_types = tuple(k for k in ('segm', 'bbox') if k in postprocessors.keys())
    coco_evaluator = CocoEvaluator(base_ds, iou_types)
    # coco_evaluator.coco_eval[iou_types[0]].params.iouThrs = [0, 0.1, 0.5, 0.75]

    for samples, targets in metric_logger.log_every(data_loader, 10, header):
        samples = samples.to(device)
        targets = [{k: v.to(device) for k, v in t.items()} for t in targets]

        outputs = model(samples)
        loss_dict = criterion(outputs, targets)
        weight_dict = criterion.weight_dict

        # reduce losses over all GPUs for logging purposes
        loss_dict_reduced = utils.reduce_dict(loss_dict)
        loss_dict_reduced_scaled = {k: v * weight_dict[k]
                                    for k, v in loss_dict_reduced.items() if k in weight_dict}
        loss_dict_reduced_unscaled = {f'{k}_unscaled': v
                                      for k, v in loss_dict_reduced.items()}
        metric_logger.update(loss=sum(loss_dict_reduced_scaled.values()),
                             **loss_dict_reduced_scaled,
                             **loss_dict_reduced_unscaled)
        metric_logger.update(class_error=loss_dict_reduced['class_error'])

        orig_target_sizes = torch.stack([t["orig_size"] for t in targets], dim=0)
        results = postprocessors['bbox'](outputs, orig_target_sizes)
        if 'segm' in postprocessors.keys():
            target_sizes = torch.stack([t["size"] for t in targets], dim=0)
            results = postprocessors['segm'](results, outputs, orig_target_sizes, target_sizes)
        res = {target['image_id'].item(): output for target, output in zip(targets, results)}

        draw_detected_img(res, targets)

        # return res, targets

def plot_results(img, prob, dt_boxes, gt_boxes, img_id, img_name):
    plt.figure(figsize=(16, 10))
    plt.imshow(img)
    ax = plt.gca()
    for p, (dt_xmin, dt_ymin, dt_xmax, dt_ymax) in zip(prob, dt_boxes.tolist()):
        ax.add_patch(plt.Rectangle((dt_xmin, dt_ymin), dt_xmax - dt_xmin, dt_ymax - dt_ymin,
                                   fill=False, color='red', linewidth=1))
        text = f'target: {p:0.2f}'
        ax.text(dt_xmin, dt_ymin-5, text, fontsize=5,
                bbox=dict(facecolor='yellow', alpha=0.0))

    for gt_xmin, gt_ymin, gt_w, gt_h in gt_boxes.tolist():
        ax.add_patch(plt.Rectangle((gt_xmin, gt_ymin), gt_w, gt_h,
                                   fill=False, color='green', linewidth=1))

    plt.axis('off')
    plt.savefig('../Detected_images/{}_{}.png'.format(str(img_id[0]), img_name), dpi=66.5, bbox_inches='tight', pad_inches=0)
    plt.close()

def find_img(img_id, dataset_path):
    id = img_id[0]
    json_path = dataset_path
    ann_file = json.load(open(json_path, "r"))
    img_anno = ann_file['images'][id]
    box_anno = ann_file['annotations']
    gt_boxes = []
    for i in range(len(box_anno)):
        if box_anno[i]['image_id'] == id:
            gt_boxes.append(box_anno[i]['bbox'])
    gt_boxes = torch.tensor(gt_boxes)
    if id == img_anno['id']:
        img_path = args.coco_path + '/val_images/' + img_anno['file_name']
        img = cv2.imread(img_path)
    else:
        print("Wrong Image ID Match!")
    return gt_boxes, img, img_anno['file_name']

def draw_detected_img(res, targets):
    img_id = targets[0]['image_id'].cpu().numpy()

    keep = res[img_id[0]]['scores'] > 0.7
    dt_boxes = res[img_id[0]]['boxes'][keep]
    dt_boxes_scores = res[img_id[0]]['scores'][keep]

    dataset_path = args.coco_path + '/annotations/instances_val.json'
    gt_boxes, img, img_name = find_img(img_id, dataset_path)
    plot_results(img, dt_boxes_scores, dt_boxes, gt_boxes, img_id, img_name)

